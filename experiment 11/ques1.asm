.MODEL SMALL
.DATA
A DB "LEFT BUTTON!$"
B DB "RIGHT BUTTON!$"  
C DB "BOTH BUTTONS!$"
.CODE
START:
MOV AX,@DATA
MOV DS,AX
L:
MOV AX,3
INT 33H
CMP BX,1
JNE  N
LEA DX,A
JMP Q
N:
CMP BX,2
JNE P
LEA DX,B
JMP Q
P:
CMP BX,3
JNE Q
LEA DX,C
Q:
CMP BX,0
JE L
MOV AH,09H
INT 21H
END START
